value = 3.14159265359

# Rounding to 2 decimal places
print(f"Rounded to 2 decimal places: {value:.2f}")

# Truncating to 2 decimal places (no rounding)
print(f"Truncated to 2 decimal places: {value:.2}")

large_number=1234567890

# With comma as thousand separator
print(f"Large number: {large_number:,}")

# With comma and decimal places
print(f"Large float: {large_number:,.2f}")

